var mongoose = require('mongoose');

// this is the collection name
var Todo = mongoose.model('Todos', { // only lower case in mongoose?
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = {Todo};
